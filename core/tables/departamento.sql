/*
* La tabla DEPARTAMENTO almacena información sobre los departamentos de Guatemala

* Campos:
* - ID_DEPARTAMENTO: Identificador único del departamento (clave primaria)
* - NOMBRE: Nombre del departamento (varchar2(100), no nulo)
* - CODIGO: Código numérico del departamento (number(5), no nulo)
*/
CREATE TABLE DEPARTAMENTO
(
    ID_DEPARTAMENTO INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOMBRE VARCHAR2(100) NOT NULL,
    CODIGO NUMBER(5) NOT NULL,
    CONSTRAINT PK_DEPARTAMENTO PRIMARY KEY (ID_DEPARTAMENTO)
);

