/*
* Tabla que almacena las respuestas de los usuarios a las preguntas de un examen
* Campos:
* - ID_RESPUESTA_USUARIO: Identificador único de la respuesta (clave primaria)
* - PREGUNTA_ID_PREGUNTA: Identificador de la pregunta a la que corresponde la respuesta 
        (clave foránea que referencia a PREGUNTA)
* - EXAMEN_ID_EXAMEN: Identificador del examen al que corresponde la respuesta (clave foránea que referencia a EXAMEN)
* - RESPUESTA: Respuesta del usuario a la pregunta (number(1), no nulo)

* Restricciones:
* - La clave primaria es ID_RESPUESTA_USUARIO.
* - PREGUNTA_ID_PREGUNTA es una clave foránea que referencia a ID_PREGUNTA en la tabla PREGUNTAS.
* - EXAMEN_ID_EXAMEN es una clave foránea que referencia a ID_EXAMEN en la tabla EXAMEN.
*/
CREATE TABLE RESPUESTA_USUARIO
(
    ID_RESPUESTA_USUARIO  INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    PREGUNTA_ID_PREGUNTA INTEGER NOT NULL,
    EXAMEN_ID_EXAMEN INTEGER NOT NULL,
    RESPUESTA NUMBER(2) NOT NULL,
    CONSTRAINT PK_RESPUESTA_USUARIO PRIMARY KEY (ID_RESPUESTA_USUARIO),
    CONSTRAINT FK_PREGUNTA_ID_PREGUNTA 
            FOREIGN KEY (PREGUNTA_ID_PREGUNTA) 
            REFERENCES PREGUNTAS(ID_PREGUNTA),
    CONSTRAINT FK_EXAMEN_ID_EXAMEN 
            FOREIGN KEY (EXAMEN_ID_EXAMEN) 
            REFERENCES EXAMEN(ID_EXAMEN)
);

