/*
* La tabla Municipio almacena información sobre los municipios asociados a cada departamento.

* Campos:
* - ID_MUNICIPIO: Identificador único del municipio (clave primaria).
* - DEPARTAMENTO_ID_DEPARTAMENTO: Identificador del departamento al que pertenece el municipio (no nulo).
* - NOMBRE: Nombre del municipio (no nulo).
* - CODIGO: Código del municipio (no nulo), valor numérico que representa el código del municipio.

* Relaciones:
* - La tabla MUNICIPIO tiene una relación de clave foránea con la tabla DEPARTAMENTO 
*   a través del campo DEPARTAMENTO_ID_DEPARTAMENTO.
*/
CREATE TABLE MUNICIPIO
(
    ID_MUNICIPIO                 INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    DEPARTAMENTO_ID_DEPARTAMENTO INTEGER       NOT NULL,
    NOMBRE                       VARCHAR2(100) NOT NULL,
    CODIGO                       NUMBER(3)     NOT NULL,

    CONSTRAINT PK_MUNICIPIO
        PRIMARY KEY (ID_MUNICIPIO, DEPARTAMENTO_ID_DEPARTAMENTO),

    CONSTRAINT FK_MUNICIPIO_DEPARTAMENTO
        FOREIGN KEY (DEPARTAMENTO_ID_DEPARTAMENTO)
        REFERENCES DEPARTAMENTO(ID_DEPARTAMENTO)
        ON DELETE CASCADE,

    CONSTRAINT UQ_MUNICIPIO_DEPARTAMENTO_CODIGO
        UNIQUE (DEPARTAMENTO_ID_DEPARTAMENTO, CODIGO),

    CONSTRAINT UQ_MUNICIPIO_DEPARTAMENTO_NOMBRE
        UNIQUE (DEPARTAMENTO_ID_DEPARTAMENTO, NOMBRE)
);

