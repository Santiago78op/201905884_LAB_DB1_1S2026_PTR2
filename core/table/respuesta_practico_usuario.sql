-- ? Crea tabla Respuesta_practico_usuario
/*
* Tabla que almacena las respuestas de los usuarios a las preguntas prácticas de un examen, junto con la nota obtenida en cada respuesta.

* Campos:
* - ID_RESPUESTA_PRACTICO: Identificador único de la respuesta (clave primaria)
* - PREGUNTA_PRACTICO_ID_PREGUNTA_PRACTICO: Identificador de la pregunta práctica a la que corresponde la respuesta 
        (clave foránea que referencia a PREGUNTA_PRACTICO)
* - EXAMEN_ID_EXAMEN: Identificador del examen al que corresponde la respuesta (clave foránea que referencia a EXAMEN)
* - NOTA: Nota obtenida por el usuario en la respuesta a la pregunta práctica (number(2, 2), no nulo)

* Restricciones:
* - La clave primaria es ID_RESPUESTA_PRACTICO.
* - PREGUNTA_PRACTICO_ID_PREGUNTA_PRACTICO es una clave foránea que referencia a ID_PREGUNTA_PRACTICO en la tabla PREGUNTA_PRACTICO.
* - EXAMEN_ID_EXAMEN es una clave foránea que referencia a ID_EXAMEN en la tabla EXAMEN.
*/
CREATE TABLE RESPUESTA_PRACTICO_USUARIO (

    ID_RESPUESTA_PRACTICO                  INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    PREGUNTA_PRACTICO_ID_PREGUNTA_PRACTICO INTEGER   NOT NULL,
    EXAMEN_ID_EXAMEN                       INTEGER   NOT NULL,
    NOTA                                   NUMBER(5,2) NOT NULL,

    CONSTRAINT PK_RESPUESTA_PRACTICO_USUARIO
        PRIMARY KEY (ID_RESPUESTA_PRACTICO),

    CONSTRAINT FK_RESPUESTA_PRACTICO_PREGUNTA
        FOREIGN KEY (PREGUNTA_PRACTICO_ID_PREGUNTA_PRACTICO)
        REFERENCES PREGUNTAS_PRACTICO (ID_PREGUNTA_PRACTICO)
        ON DELETE CASCADE,

    CONSTRAINT FK_RESPUESTA_PRACTICO_EXAMEN
        FOREIGN KEY (EXAMEN_ID_EXAMEN)
        REFERENCES EXAMEN (ID_EXAMEN)
        ON DELETE CASCADE
);

